const viTranslations: Record<string, string> = Object.fromEntries(
  Object.entries(viTranslationsRaw).map(([key, value]) => [
    repairMojibake(key),
    repairMojibake(value),
  ]),
);

Object.assign(viTranslations, viTranslationOverrides, adminCustomerTranslationOverrides);

function detectBrowserLocale(): Locale {
  if (typeof window === 'undefined') {
    return 'vi';
  }

  const saved = window.localStorage.getItem(STORAGE_KEY);

  if (saved === 'en' || saved === 'vi') {
    return saved;
  }

  return window.navigator.language.toLowerCase().startsWith('vi') ? 'vi' : 'en';
}

export function translateText(locale: Locale, text: string) {
  const normalizedText = repairMojibake(text);

  if (locale === 'en') {
    return normalizedText;
  }

  const translated = viTranslations[normalizedText] ?? viTranslations[text] ?? normalizedText;

  return isBrokenDisplayText(translated) ? normalizedText : translated;
}

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [locale, setLocale] = useState<Locale>('vi');

  useEffect(() => {
    setLocale(detectBrowserLocale());
  }, []);

  useEffect(() => {
    if (typeof window !== 'undefined') {
      window.localStorage.setItem(STORAGE_KEY, locale);
      document.documentElement.lang = locale;
    }
  }, [locale]);

  const value = useMemo<I18nContextValue>(
    () => ({
      locale,
      setLocale,
      tt: (text) => translateText(locale, text),
    }),
    [locale],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);

  if (!context) {
    throw new Error('useI18n must be used inside I18nProvider');
  }

  return context;
}
